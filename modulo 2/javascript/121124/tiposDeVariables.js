// Declaración y asignación de variables con let
let city = "Vigo"
console.log("Esta es una variable con let: ", city)

// Declaración de variable constante con const
const isStudent = true
console.log("Esta es una variable con Const: ", isStudent)

// Declaración de variable con var
var colorCar = "blue"
console.log("Esta es una variable con var: ", colorCar)

//Concatena dos o más variables
let name = "Pepe"
let lastName = "noLoSe"
console.log(`user: ${name} ${lastName}`)

// Concatenación de cadenas utilizando el operador +
let vehicle = "avion"
let isFree = false
let isExpensive = true
console.log("Medio de trasporte: " + vehicle + ", ¿es gratis? " + isFree + ", ¿Es caro? " + isExpensive)