// Math redondear un numero

let numero = 4.2

// ---  Redondear numeros
 
// Para redondear al entero mas cercano utilizamos Math.round() 
console.log(Math.round(numero))

// Para Redondear el numero hacia abajo utilizamos Math.floor()
console.log(Math.floor(numero))

// Para Redondear el numero hacia arriba utilizamos Math.floor()
console.log(Math.ceil(numero))

// Eliminar el decimal y dejar los enteros utilizamos Math.trunc()
console.log(Math.trunc(numero))


