// Diseño Grafico vs Diseño Web

/* 
- Diseño Grafico
   - Enfocado en medios impresos
   - Herramientas: Photoshop, Illustrator

- Diseño Web
   - Interfaces digitales interactivas
   - Uso de HTML, CSS y JavaScript
   - Adaptabilidad y funcionalidad 
*/

// Principios del diseño Web

/* 
- Usabilidad:
   - Navegacion intuitiva, velocidad de carga

- Accesibilidad: 
   - Inclusion de usuarios con discapacidades
   
- UX:
   - Experiencia general del usuario

- Diseño Adaptativo:
   - Ajuste a dispositivos diversos
*/

// Proceso de diseño web

/*
- Investigacion:
   - Publico objectivo, competidores

- Planificacion:
   -Sitemap, flujo de navegacion.

- Wireframes: 
   - Diseño estructural basico.

- Prototipos: 
   - Versiones interactivas para pruebas.
*/

